public class DataType {
    public static void main(String[] args) {
        // Boolean
        boolean boolVal = true;

        // Integer Types
        byte byteVal = 127;
        short shortVal = 32767;
        int intVal = 2147483647;
        long longVal = 9223372036854775807L;

        // Floating Point Types
        float floatVal = 3.14f;
        double doubleVal = 3.1415926535;

        // Character
        char charVal = 'A';

        // String
        String strVal = "Hello, Java!";

        // Print all data types
        System.out.println("Boolean: " + boolVal);
        System.out.println("Integer Types:");
        System.out.println("  Byte: " + byteVal);
        System.out.println("  Short: " + shortVal);
        System.out.println("  Int: " + intVal);
        System.out.println("  Long: " + longVal);
        System.out.println("Floating Point Types:");
        System.out.println("  Float: " + floatVal);
        System.out.println("  Double: " + doubleVal);
        System.out.println("Character: " + charVal);
        System.out.println("String: " + strVal);
    }
}
